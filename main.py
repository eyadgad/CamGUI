    # BUTTONS CLICK
    # Post here your functions for clicked buttons
    # ///////////////////////////////////////////////////////////////
    def buttonClick(self):
        # GET BUTTON CLICKED
        btn = self.sender()
        btnName = btn.objectName()

        # SHOW HOME PAGE
        if btnName == "btn_home":
            widgets.stackedWidget.setCurrentWidget(widgets.home)
            UIFunctions.resetStyle(self, btnName)
            btn.setStyleSheet(UIFunctions.selectMenu(btn.styleSheet()))

        # SHOW WIDGETS PAGE
        if btnName == "btn_widgets":
            widgets.stackedWidget.setCurrentWidget(widgets.widgets)
            UIFunctions.resetStyle(self, btnName)
            btn.setStyleSheet(UIFunctions.selectMenu(btn.styleSheet()))

        # SHOW NEW PAGE
        if btnName == "btn_new":
            widgets.stackedWidget.setCurrentWidget(widgets.new_page) # SET PAGE
            UIFunctions.resetStyle(self, btnName) # RESET ANOTHERS BUTTONS SELECTED
            btn.setStyleSheet(UIFunctions.selectMenu(btn.styleSheet())) # SELECT MENU

        # PRINT BTN NAME
        print(f'Button "{btnName}" pressed!')


    def loadImage(self):
        vid = cv2.VideoCapture(1)
        while(True):
            img, image = vid.read()
            self.setPhoto(image)
            
            key = cv2.waitKey(1) & 0xFF
            if not self.check:break
        cv2.destroyAllWindows

    def setPhoto(self,image):
        qformat=QImage.Format_Indexed8
        if len(image.shape)==3:
            if image.shape[2]==4: qformat= QImage.Format_RGBA888
            else:qformat= QImage.Format_RGB888
        img= QImage(image, image.shape[1],image.shape[0],image.strides[0],qformat)
        img= img.rgbSwapped()
        widgets.vidlabel.setPixmap(QPixmap.fromImage(img))
        if self.snapcheck:
            self.snapcheck=False
            self.temp=image
            widgets.imglabel.setPixmap(QPixmap.fromImage(img))
 

